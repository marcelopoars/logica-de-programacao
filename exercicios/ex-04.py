'''
     04 - Ler três valores numéricos e escrever a média aritmética.
'''

value1 = float(input("Digite o primeiro valor: "))
value2 = float(input("Digite o segundo valor: "))
value3 = float(input("Digite o terceiro valor: "))

average = (value1 + value2 + value3) / 3

print(f"A média aritimética dos valores é {average}")
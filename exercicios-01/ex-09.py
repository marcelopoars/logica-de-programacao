'''
    Faça um algoritmo que mostre o resultado da expressão abaixo:

    (( x - 5) * y) - z

    Obs: Ler valores para as variáveis x, y e z.
'''

x = float(input("Digite um valor para 'x': "))
y = float(input("Digite um valor para 'y': "))
z = float(input("Digite um valor para 'z': "))

result =  (( x - 5) * y) - z

print(f"O resultado da expressão (( x - 5) * y) - z é: {result}")